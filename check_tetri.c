#include "libft.h"

int	check_tetri(char *s, int i)
{
	int	j;
	int	n;

	while (s[i])
	{
		j = 0;
		while (s[i] != '\n')
		{
			if (s[i] != '.' && s[i] != '#')
				return (0);
			i++;
			j++;
		}
		n++;
		if (j != 4)
			return (0);
		else
			i++;
	}
	if (n != 4)
		return (0);
	return (1);
}
